import math

def tambah(a, b):
    tambahh = a + b
    print("Penjumlahan:",tambahh)

def kurang(a, b):
    kurangg = a - b
    print("Pengurangan:",kurangg)

def kali(a, b):
    kalii = a * b
    print("Perkalian:",kalii)

def bagi(a, b):
    bagii = a / b
    print("Pembagian:",bagii)

def akar(a):
    akarr = math.sqrt(a)
    print("Akar:",akarr)

def pangkat(a, b):
    pangkatt = math.pow (a, b)
    print("Pangkat:",pangkatt)

def sinus(a):
    sin = math.sin(a)
    print("Sinus:",sin)

def cosinus(a):
    cos =  math.cos(a)
    print("Cosinus:",cos)

def tangen(a):
    tan = math.tan(a)
    print("Tangen:",tan)

